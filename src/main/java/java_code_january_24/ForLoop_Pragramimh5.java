package java_code_january_24;

public class ForLoop_Pragramimh5 {

	public static void main(String[] args) {
		
		//print 11 multiples of 2 in decreasing order starting from 20
		//20,18,16,14,12,10,8,6,4,2,0
		
		for(int i=20; i>=0; i--) {     // i=i-2
			System.out.println(i +" ");
			i--;
		}

	}

}
