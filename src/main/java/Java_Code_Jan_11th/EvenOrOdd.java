package Java_Code_Jan_11th;

public class EvenOrOdd {
	
	//WAP to find which number is even and which is Odd

	public static void main(String[] args) {
		
		int a =100;  int b =9;
		
		if(a%2 == 0)
		{
			System.out.println( "a is even number: "+ a);
		}else
		{
			System.out.println("a is odd number: "+ a);
		}
		if(b%2 == 0)
		{
			System.out.println("b is even number: "+ b);
		}else{
		
			System.out.println("b is odd number: "+b);
		}
		

	}

}
